package com.example.spb.entity;

public class Follow {

    public int id;
    public String user_accountl;
    public String user_date;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser_accountl() {
        return user_accountl;
    }

    public void setUser_accountl(String user_accountl) {
        this.user_accountl = user_accountl;
    }

    public String getUser_date() {
        return user_date;
    }

    public void setUser_date(String user_date) {
        this.user_date = user_date;
    }
}
